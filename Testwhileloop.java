/*the syntax of a while loop is 
while(Boolean_expression)
{
statements or expressions
}*/
public class Testwhileloop{

	public static void main(String args[]){
	int x = 10;

	while( x < 20 ) {
		System.out.print("value of x : " + x );
		x++; /*x++ means add x+1*/
		System.out.print("\n");	
		}	
	}
}
